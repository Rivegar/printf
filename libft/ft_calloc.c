#include "libft.h"

void	*ft_calloc(size_t nitems, size_t size)
{
	void	*tmp;

	tmp = malloc(nitems * size);
	if (!tmp)
		return (NULL);
	ft_bzero(tmp, nitems * size);
	return (tmp);
}
